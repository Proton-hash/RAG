def greet(name):
    """
    A simple function that greets a person by name.
    
    Args:
        name (str): The name of the person to greet
        
    Returns:
        str: A greeting message
    """
    return f"Hello, {name}! Welcome to Python."


# Example usage
if __name__ == "__main__":
    print(greet("World"))
    print(greet("Python Developer"))
